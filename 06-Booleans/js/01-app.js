//* Estos valores se utilizan para representar la verdad o la falsedad en las condiciones y decisiones dentro de un programa. Veamos algunos ejemplos simples: Uso de true y false en declaraciones condicionales:

const esMayorDeEdad = true;
const tienePermiso = false;

if (esMayorDeEdad) {
	console.log('Puede ingresar al sitio web para adultos.');
} else {
	console.log('Lo siento, debes ser mayor de edad para ingresar.');
}

if (tienePermiso) {
	console.log('Puede acceder al recurso.');
} else {
	console.log('No tienes permiso para acceder.');
}
